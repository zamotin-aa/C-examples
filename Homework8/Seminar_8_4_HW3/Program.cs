// Задача 62. Напишите программу, которая заполнит спирально массив 4 на 4.


// Создаем массив 4 на 4
int[,] array = new int[4, 4];

// Задаем начальные значения для направления и координат
int direction = 0; // 0 - вправо, 1 - вниз, 2 - влево, 3 - вверх
int x = 0; // Координата по горизонтали
int y = 0; // Координата по вертикали

// Заполняем массив спирально от 1 до 16
for (int i = 1; i <= 16; i++)
{
    // Присваиваем текущему элементу массива значение i
    array[x, y] = i;

    // Изменяем координаты и направление в зависимости от текущего положения
    switch (direction)
    {
        case 0: // Если движемся вправо
            if (x < 3 && array[x + 1, y] == 0) // Если не достигли правого края и следующий элемент пустой
            {
                x++; // Двигаемся вправо
            }
            else // Иначе
            {
                direction = 1; // Меняем направление на вниз
                y++; // Двигаемся вниз
            }
            break;
        case 1: // Если движемся вниз
            if (y < 3 && array[x, y + 1] == 0) // Если не достигли нижнего края и следующий элемент пустой
            {
                y++; // Двигаемся вниз
            }
            else // Иначе
            {
                direction = 2; // Меняем направление на влево
                x--; // Двигаемся влево
            }
            break;
        case 2: // Если движемся влево
            if (x > 0 && array[x - 1, y] == 0) // Если не достигли левого края и следующий элемент пустой
            {
                x--; // Двигаемся влево
            }
            else // Иначе
            {
                direction = 3; // Меняем направление на вверх
                y--; // Двигаемся вверх
            }
            break;
        case 3: // Если движемся вверх
            if (y > 0 && array[x, y - 1] == 0) // Если не достигли верхнего края и следующий элемент пустой
            {
                y--; // Двигаемся вверх
            }
            else // Иначе
            {
                direction = 0; // Меняем направление на вправо
                x++; // Двигаемся вправо
            }
            break;
    }
}

// Выводим массив на экран
for (int i = 0; i < 4; i++)
{
    for (int j = 0; j < 4; j++)
    {
        Console.Write(array[j, i] + "\t");
    }
    Console.WriteLine();
}
